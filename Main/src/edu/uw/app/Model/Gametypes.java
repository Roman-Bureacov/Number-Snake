package edu.uw.app.Model;

/**
 * Enum containing the different recognized game types to be fed to the game.
 * @author Roman Bureacov
 * @version 2025-03-27
 */
public enum Gametypes {
    /** sum the numbers to the target. */
    TYPE_SUM,
    /** multiply the numbers to the target. */
    TYPE_PROD,
    /** starting from a number, subtract from the subsequent numbers to the target. */
    TYPE_SUB,
}
